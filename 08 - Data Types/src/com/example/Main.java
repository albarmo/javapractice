package com.example;

public class Main {
public static void main(String[] args){

//    tipedata di bahasa pemrograman java
//    String bukan data primitive

//    primitive data types di java
//    integer, byte, short, long, double, float, char, boolean

//    integer
    int i = 10;
    System.out.println("ini tipedata integer : " + i);
    System.out.println("max integer " + Integer.MAX_VALUE);
    System.out.println("min integer " + Integer.MIN_VALUE);
    System.out.println("Besar Integer :" + Integer.BYTES + " bytes");
    System.out.println("Besar Integer : " + Integer.SIZE + " bit");
    System.out.println("\n");

    //    Byte
    byte b = 10;
    System.out.println("ini tipedata integer : " + b);
    System.out.println("max integer " + Byte.MAX_VALUE);
    System.out.println("min integer " + Byte.MIN_VALUE);

    System.out.println("Besar Integer :" + Byte.BYTES + " bytes");
    System.out.println("Besar Integer : " + Byte.SIZE + " bit");
    System.out.println("\n");

    //    Short
    byte s = 10;
    System.out.println("ini tipedata integer : " + s);
    System.out.println("max integer " + Short.MAX_VALUE);
    System.out.println("min integer " + Short.MIN_VALUE);
    System.out.println("Besar Integer :" + Short.BYTES + " bytes");
    System.out.println("Besar Integer : " + Short.SIZE + " bit");

    System.out.println("\n");

    //    Long
    long l = 10L;
    System.out.println("ini tipedata integer : " + l);
    System.out.println("max integer " + Long.MAX_VALUE);
    System.out.println("min integer " + Long.MIN_VALUE);
    System.out.println("Besar Integer :" + Long.BYTES + " bytes");
    System.out.println("Besar Integer : " + Long.SIZE + " bit");
    System.out.println("\n");

    //    Double
    double d = 10.5d;
    System.out.println("ini tipedata integer : " + d);
    System.out.println("max integer " + Double.MAX_VALUE);
    System.out.println("min integer " + Double.MIN_VALUE);
    System.out.println("Besar Integer :" + Double.BYTES + " bytes");
    System.out.println("Besar Integer : " + Double.SIZE + " bit");
    System.out.println("\n");


    //    Float
    float f = 10.5f;
    System.out.println("ini tipedata integer : " + f);
    System.out.println("max integer " + Float.MAX_VALUE);
    System.out.println("min integer " + Float.MIN_VALUE);
    System.out.println("Besar Integer :" + Float.BYTES + " bytes");
    System.out.println("Besar Integer : " + Float.SIZE + " bit");
    System.out.println("\n");

    //    Char
    char c = 'c';
    System.out.println("ini tipedata integer : " + c);
    System.out.println("max integer " + Character.MAX_VALUE);
    System.out.println("min integer " + Character.MIN_VALUE);
    System.out.println("Besar Integer :" + Character.BYTES + " bytes");
    System.out.println("Besar Integer : " + Character.SIZE + " bit");
    System.out.println("\n");

    //    Char
    boolean value = true;
    System.out.println("ini tipedata integer : " + value);
    System.out.println("\n");


}
}
